# 收集所有指标名
all_indicators = {}
for std_id, std_data in standards.items():
    for ind_name, cond, val, unit, grade in std_data['indicators']:
        # 规范化指标名作为文件名
        ind_key = ind_name.replace(' ', '_').replace('(', '（').replace(')', '）').replace('/', '／')
        if ind_key not in all_indicators:
            all_indicators[ind_key] = ind_name

# 创建指标页面
for ind_key, ind_name in all_indicators.items():
    # 找到引用此指标的标准
    ref_stds = []
    for std_id, std_data in standards.items():
        for i_name, _, _, _, _ in std_data['indicators']:
            if i_name == ind_name:
                ref_stds.append(std_id)
    
    content = f"""---
title: {ind_name}
type: indicator
---

# {ind_name}

## 引用此指标的标准

"""
    for sid in ref_stds:
        content += f'- [[wiki/standards/{sid}|{sid.replace("-", "/")}]]\n'
    
    with open(os.path.join(IND_DIR, f'{ind_key}.md'), 'w', encoding='utf-8') as f:
        f.write(content)

# 创建标准页面
for std_id, std_data in standards.items():
    std_no = std_id.replace('-', '/', 1)  # GBT-2440-2017 -> GB/T-2440-2017
    std_no = std_no.replace('GB/', 'GB/T') if 'T' in std_id else std_no.replace('GB/', 'GB/')
    
    # 规范化标准号显示
    display_no = std_id.replace('-', '/', 1)
    if 'T' in display_no:
        display_no = display_no.replace('GB/', 'GB/T')
    
    content = f"""---
title: {display_no} {std_data['name']}
type: standard
category: {std_data['category']}
---

# {display_no} {std_data['name']}

## 技术指标

| 指标名称 | 约束 | 数值 | 单位 | 等级 |
|---------|:----:|:----:|:----:|:----:|
"""
    for ind_name, cond, val, unit, grade in std_data['indicators']:
        ind_key = ind_name.replace(' ', '_').replace('(', '（').replace(')', '）').replace('/', '／')
        content += f"| [[wiki/indicators/{ind_key}|{ind_name}]] | {cond} | {val} | {unit} | {grade} |\n"
    
    if std_data['refs']:
        content += "\n## 引用标准\n\n"
        for ref in std_data['refs']:
            content += f"- [[wiki/standards/{ref}|{ref.replace('-', '/')}]]\n"
    
    content += f"\n## 所属领域\n\n- [[wiki/concepts/{std_data['category'] == '肥料' and '肥料' or std_data['category']}]]\n"
    
    with open(os.path.join(STD_DIR, f'{std_id}.md'), 'w', encoding='utf-8') as f:
        f.write(content)

print(f'Created {len(standards)} standard pages')
print(f'Created {len(all_indicators)} indicator pages')
